package operators;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		int a = 30;
		int b = 40;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(b!=a);
		System.out.println(b==a);

	}

}
